import { Aes } from "#crypto/aes/Aes.js";
import { WordArray } from "#crypto/aes/WordArray.js";
import { vectorsOf } from "./vector-utils.js";

describe("Aes", () => {
    it("correctly encrypts all NIST test vectors", () => {
        for (const { key, plaintext, ciphertext } of vectorsOf(nistVectors)) {
            const p = WordArray.fromByteArray(plaintext);
            const c = WordArray.fromByteArray(ciphertext);
            const aes = Aes(key);
            expect(aes.encrypt(p)).deep.equals(c);
        }
    });

    it("correctly decrypts all NIST test vectors", () => {
        for (const { key, plaintext, ciphertext } of vectorsOf(nistVectors)) {
            const p = WordArray.fromByteArray(plaintext);
            const c = WordArray.fromByteArray(ciphertext);
            const aes = Aes(key);
            expect(aes.decrypt(c)).deep.equals(p);
        }
    });
});

